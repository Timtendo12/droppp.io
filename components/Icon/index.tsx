type Props = {
  className?: string
  name: Icons | string
  size?: number
  style?: React.CSSProperties
  color?: string
}

export default function Icon({ name, size, ...rest }: Props) {
  const Component = ICONS[name]
  // if using image instead of svg file, clone w/ any required props
  if (ICONS_IMAGES[name]) return React.cloneElement(Component, rest)
  // return old icon if not exists
  if (!Component) return <OldIcon name={name} size={size} {...rest} />
  if (size) return <Component width={size} height={size} {...rest} />

  return <Component {...rest} />
}
